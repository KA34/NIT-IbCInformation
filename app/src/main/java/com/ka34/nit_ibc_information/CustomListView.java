package com.ka34.nit_ibc_information;

public class CustomListView {
    private String date;
    private String type;
    private String term;
    private String cont;
    private String clas;

    public void setDate(String date1){
        this.date = date1;
    }
    public String getDate(){
        return date;
    }
    public void setType(String type1){
        this.type = type1;
    }
    public String getType(){
        return type;
    }
    public void setTerm(String term1){
        this.term = term1;
    }
    public String getTerm(){
        return term;
    }
    public void setCont(String cont1){
        this.cont = cont1;
    }
    public String getCont(){
        return cont;
    }
    public void setClas(String clas1){
        this.clas = clas1;
    }
    public String getClas(){
        return clas;
    }
}


